from sympy import S, pi, sqrt, elliptic_e as E

def R1(a,b):
    h = (S(a-b)/(a+b))**2
    return pi*(a+b)*(3-sqrt(4-h))

def R2(a,b):
    h = (S(a-b)/(a+b))**2
    return pi*(a+b)*(1+3*h/(10+sqrt(4-h)))

def circumference(a,b):
    e = sqrt(a**2-b**2)/a
    return 4*a*E(e)

